package com.sarxos.gpwnotifier.data;


public class QuotesReaderException extends Exception {

	private static final long serialVersionUID = 2147500750172014553L;

	public QuotesReaderException() {
		super();
	}

	public QuotesReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public QuotesReaderException(String message) {
		super(message);
	}

	public QuotesReaderException(Throwable cause) {
		super(cause);
	}
}
